package com.coreJava.switchCase;

public class switchCase {

	public static void main(String[] args) {
		char ch='A';

		switch (ch) {
		case 'a':
			System.out.println("hello");

		default:
			System.out.println("default");
			
		case 'A':
			System.out.println("hi");

		}

	}

}
